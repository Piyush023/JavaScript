// For Loop - 
for(let i=0;i<=10;i++){
    if(i===9){
        break;
    }
    if(i===5){
        continue
    }
    console.log(i);
}

// While Loop - 

let i = 0;
while(i<10){
    console.log(i)
    i++
}

// Do-While Loop - 

let index = 0;
do{
    console.log(index);
    index++
} while(index<10);
